class UserModel {
  final String name;
  final String bio;
  final String location;
  final String vibe;
  final String age;
  final String height;
  final String relationshipStatus;
  final String imageUrl;

  UserModel({
    required this.name,
    required this.bio,
    required this.location,
    required this.vibe,
    required this.age,
    required this.height,
    required this.relationshipStatus,
    required this.imageUrl,
  });
}